public class ATIVIDADE_001_Boleano {
    public static void main(String[] args) {


        // Coroa é determinado uma variável boleana verdadeira.
        boolean coroa = true;

        // Coroa sendo verdadeiro, será executado o print.
        if (coroa) {
            System.out.println("COROA");

        }
        // Else não será executado, visto que coroa é verdadeiro.
        else  {
            System.out.println("CARA");
        }
    }
}
