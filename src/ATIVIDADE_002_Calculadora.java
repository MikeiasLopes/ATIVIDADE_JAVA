import java.util.Scanner;

public class ATIVIDADE_002_Calculadora {
    public static void main(String[] args) {

        int multiplicador = 9;
        System.out.println("Tabuada do " + multiplicador);

        for (int i = 0; i <= 10; i++) {
            System.out.println(i + " X " + multiplicador + " = " + i * multiplicador);
        }

    }
}


